# {'A': 250, 'B': 200, 'C': 300}
def merge_sales(q1, q2):
    res = q1.copy()  # –∫–æ–ø–∏—Ä–∞–º–µ –ø—ä—Ä–≤–∏—è —Ä–µ—á–Ω–∏–∫, –∑–∞ –¥–∞ –Ω–µ –≥–æ –ø—Ä–æ–º–µ–Ω—è–º–µ
    for k, v in q2.items():
        res[k] = res.get(k, 0) + v  # –∞–∫–æ –ø—Ä–æ–¥—É–∫—Ç—ä—Ç –ª–∏–ø—Å–≤–∞, –∑–∞–ø–æ—á–≤–∞–º–µ –æ—Ç 0
    return res


# --- –ü—Ä–∏–º–µ—Ä–µ–Ω —Ç–µ—Å—Ç ---
sales_q1 = {"A": 100, "B": 200}
sales_q2 = {"A": 150, "C": 300}

print(merge_sales(sales_q1, sales_q2))
