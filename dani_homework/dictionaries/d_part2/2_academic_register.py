def best_student(students):
    best_name = None
    best_avg = 0

    for name, grades in students.items():
        if grades:  # Ğ°ĞºĞ¾ Ğ¸Ğ¼Ğ° Ğ¾Ñ†ĞµĞ½ĞºĞ¸
            avg = sum(grades) / len(grades)
            if avg > best_avg:
                best_avg = avg
                best_name = name

    return best_name, round(best_avg, 2)


