def travel_cost(trips):
    total_costs = {}
    max_name = None
    max_cost = 0

    for name, data in trips.items():
        # data = {'–¥–µ—Å—Ç–∏–Ω–∞—Ü–∏—è': ..., '–¥–Ω–∏': ..., '—Ü–µ–Ω–∞_–Ω–∞_–¥–µ–Ω': ...}
        cost = data['–¥–Ω–∏'] * data['—Ü–µ–Ω–∞_–Ω–∞_–¥–µ–Ω']
        total_costs[name] = cost

        if cost > max_cost:
            max_cost = cost
            max_name = name

    return total_costs, max_name


